def list_to_tuples(l, n):
    """Convert a flat list of tuples into a real list of tuples

    e.g. [1, 'one', 2, 'two' ...] becomes: [(1, 'one'), (2, 'two')]

    l - the list itself
    n - the number of elements that each tuple must have

    """
    return zip(*[l[i::n] for i in range(n)])
